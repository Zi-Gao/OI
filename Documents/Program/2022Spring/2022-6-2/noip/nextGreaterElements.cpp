#include <cstdio>
#include <algorithm>
#define file
using namespace std;
inline int read();
int arr[10010];
int main(){
	#ifdef file
	freopen("nextGreaterElements.in", "r", stdin);
	freopen("nextGreaterElements.out", "w", stdout);
	#endif

	int n=0;
	int t;
	//read
	while(~scanf("%d",&t)){
		arr[n++]=t;
	}
	
	for(int i=0;i<n;i++){//i->n的f(i)计算 
		for(int j=i+1;j!=i;j++){//寻找 数列中第i个元素之后第一个大于ai的元素--j
			if(j==n){
				j=0;
			} 
			if(arr[j]>arr[i]){//如果aj元素大于ai输出 
				printf("%d ",j+1);
				goto loop;//没找的输0的处理 顺带解决break 
			}
		}
		printf("-1 ");//没goto--没找到 
		loop:;
	}

	#ifdef file
	fclose(stdin);
	fclose(stdout);
	#endif
    return 0;
}
inline int read(){
    int x=0,f=1;char c=getchar();
    while(c<'0'||c>'9')c=='-'?f=-1:f,c=getchar();//?=if,:=else
    while(c>='0'&&c<='9'){
        x=(x<<3)+(x<<1)+(c&15);
        c=getchar();
    }
    return x*f;
}
